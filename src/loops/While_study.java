package loops;

public class While_study {

	public static void main(String[] args) {
		int i=15;
		while(i>=1){
			System.out.println("oone");
			i--;
		}
		int a=5;
		while(a<=9) {
			System.out.println("two");
			a++;
		}
		int b=10;
		while(b>=1) {
			System.out.println("Hitesh");
			b--;
		}
		int c=1;
		while(c<=10) {
			System.out.println(c*2);
			c++;
		}
		int d=40;
		while(d>=35) {
			System.out.println("Sara");
			d--;
		}
		int e=2;
		while(e<=10) {
			System.out.println(e*2);
			e++;
		}
		int f=8;
		while(f<50) {
			f=f+2;
			System.out.println(f);
		}
		float g=40;
		while(g>=20) {
			System.out.println(g/2);
			g--;
		}
		int h=100;
		while(h>70) {
			System.out.println(1*h);
			h--;
		}
		
		}
	}


