package loops;

public class For_study {

	public static void main(String[] args) {
		// i want to print 
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		for(int b=30; b>=25; b--) {
			System.out.println("Hitesh");
		}
		for(int a=1; a<=10; a++) {
			System.out.println(a*2);
		}
		for(int c=11; c>=1; c--) {
			System.out.println(c*2);
		}
		for(int i=0; i<=10; i++) {
			System.out.println(2*i+1);
		}
		for(int i=0; i<=10; i++) {
			System.out.println("=============");
			System.out.println(2*i);
		}
		for(float w=10; w>=1; w--) {
			System.out.println(w/2);
		}
		
			

	}

}
